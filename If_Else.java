// The if - else statement
// Syntax
/*
if (condition) {
    block of the code
} else {
    block of the code
}
*/
public class If_Else {
    public static void main(String[] args) {
        int time = 20;
        if (time < 18) {
            System.out.println("Good day.");
        } else {
            System.out.println("Good evening");
        }
    }
}
