// Java While Loop: Through a black of code as
// long as a specified condition is true
// Syntax
/*
while (condition) {
    // code block
}
 */

public class While {
    public static void main(String[] args) {
        int i = 0;
        while (i < 5) {
            i++;
            System.out.println(i);
            // i++;
        }
    }
}
