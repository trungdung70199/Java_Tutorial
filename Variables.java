// Display Variables
// Can also use the + character to add a variable to another variable
// Declare Many Variables

public class Variables {
    public static void main(String[] args) {
        String name = "Dug";
        System.out.println("Hello " + name);

        String firstname = "Dung. ";
        String lastname = "Trung";
        String fullname = firstname + lastname;
        System.out.println(fullname);

        //int variables
        int x = 5;
        int y = 10;
        int z = 15;
        System.out.println(x + y + z);
    }
}
// The general rule for naming variables:
// Names can contain letters, digits, underscores, dollar signs
// Names must begin with a letter
// Name should start with a lowercase letter 
