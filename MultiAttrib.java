// Multiple Attributes
// Can specify as many attribute as want
public class MultiAttrib {
    String fname = "John";
    String lname = "Jack";
    int age = 24;
    public static void main(String[] args) {
        MultiAttrib myObj = new MultiAttrib();
        System.out.println("Name: " + myObj.fname + " " + myObj.lname);
        System.out.println("Age: " + myObj.age);
    }
}
